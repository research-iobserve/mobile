package org.iobserve.analysis.constraint.model;

public class ProvideExpression {

	private String name;
	private String pcmPath;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the pcmPath
	 */
	public String getPcmPath() {
		return pcmPath;
	}

	/**
	 * @param pcmPath the pcmPath to set
	 */
	public void setPcmPath(String pcmPath) {
		this.pcmPath = pcmPath;
	}

}
